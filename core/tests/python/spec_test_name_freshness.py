def _kind_summary(citations: list[Citation], expansions: int) -> str:
    counts: dict[str, int] = defaultdict(int)
    for c in citations:
        counts[c.kind] += 1
    parts = []
    for kind in ("RICH", "BRACE", "CONTINUATION", "QUALIFIED", "PATH_BARE"):
        if counts.get(kind):
            label = kind
            if kind == "BRACE" and expansions:
                label = f"BRACE→{expansions}"
            parts.append(f"{counts[kind]} {label}")
    return ", ".join(parts)


def render_human(
    docs_scanned: int,
    pre_expansion: list[Citation],
    expansions: int,
    results: list[ResolutionResult],
    *,
    verbose: bool,
    quiet: bool,
    suppressed_count: int = 0,
    stale_allowlist: list[AllowlistEntry] | None = None,
) -> tuple[str, bool]:
    """Render the human report. Returns (text, all_resolved)."""
    lines: list[str] = []
    unresolved = [r for r in results if r.status != "RESOLVED"]
    resolved = [r for r in results if r.status == "RESOLVED"]
    stale_allowlist = stale_allowlist or []
    all_ok = not unresolved and not stale_allowlist

    if quiet:
        if all_ok:
            return ("PASS\n", True)
        parts = []
        if unresolved:
            parts.append(f"{len(unresolved)} unresolved citation(s)")
        if stale_allowlist:
            parts.append(f"{len(stale_allowlist)} stale allowlist entry/entries")
        return (f"FAIL: {'; '.join(parts)}\n", False)

    lines.append("--- Section 1: doc citation index ---")
    lines.append(
        f"scanned {docs_scanned} docs, extracted {len(pre_expansion)} citations ({_kind_summary(pre_expansion, expansions)})"
    )
    lines.append("")
    lines.append("--- Section 2: resolution ---")
    suffix = f" ({suppressed_count} suppressed by allowlist)" if suppressed_count else ""
    lines.append(f"{len(resolved)} resolved, {len(unresolved)} unresolved{suffix}")

    if unresolved:
        lines.append("")
        lines.append("--- Unresolved ---")
        # Group by doc, then by status.
        by_doc: dict[Path, list[ResolutionResult]] = defaultdict(list)
        for r in unresolved:
            by_doc[r.citation.doc_path].append(r)
        for doc_path in sorted(by_doc, key=lambda p: str(p)):
            try:
                rel = doc_path.relative_to(REPO_ROOT)
            except ValueError:
                rel = doc_path
            lines.append(str(rel))
            for r in sorted(by_doc[doc_path], key=lambda r: (r.status, r.citation.line_no)):
                lines.append(f"  L{r.citation.line_no} [{r.status}] `{r.citation.raw_span}`")
                lines.append(f"    {r.detail}")

    if verbose and resolved:
        lines.append("")
        lines.append("--- Resolved ---")
        for r in resolved:
            try:
                rel = r.citation.doc_path.relative_to(REPO_ROOT)
            except ValueError:
                rel = r.citation.doc_path
            lines.append(f"  {rel}:L{r.citation.line_no} `{r.citation.raw_span}` → {r.detail}")

    if stale_allowlist:
        lines.append("")
        lines.append("--- Stale allowlist entries (citation now resolves; remove from allowlist) ---")
        for entry in stale_allowlist:
            lines.append(f"  L{entry.source_line} {entry.key}")
            lines.append(f"    reason was: {entry.reason}")

    lines.append("")
    if all_ok:
        lines.append("PASS")
    else:
        parts = []
        if unresolved:
            parts.append(f"{len(unresolved)} unresolved citation(s)")
        if stale_allowlist:
            parts.append(f"{len(stale_allowlist)} stale allowlist entry/entries")
        lines.append(f"FAIL: {'; '.join(parts)}")
    return ("\n".join(lines) + "\n", all_ok)


