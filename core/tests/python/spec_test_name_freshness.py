CitationKind = Literal["RICH", "BRACE", "CONTINUATION", "QUALIFIED", "PATH_BARE"]


@dataclass(frozen=True)
class Citation:
    """One extracted backtick span, classified for resolution.

    `name` semantics by kind:
      RICH         - bare fn name (`path_hint` carries the .rs file).
      BRACE        - never directly emitted; expanded to per-element citations
                     (each becoming RICH if the brace prefix had a path or
                     PATH_BARE otherwise) before downstream resolution.
      CONTINUATION - the `_suffix_with_underscores` part; resolution must
                     pair it with the most recent non-CONTINUATION citation
                     on the same line (the "anchor") via a strip-N search.
      QUALIFIED    - the variant/const name (`path_hint` carries the type).
      PATH_BARE    - the bare snake_case name.
    """

    doc_path: Path
    line_no: int
    raw_span: str
    kind: CitationKind
    name: str
    path_hint: str | None  # for RICH: rust file; QUALIFIED: type; CONTINUATION: anchor.name; else None
    anchor_file: str | None = None  # for CONTINUATION: anchor.path_hint when anchor was RICH


# Spans containing any of these characters are treated as descriptive prose,
# not identifier candidates. Filters out command lines, formulas, paths-as-prose.
_SPAN_REJECT_CHARS = frozenset(" \t()=<>→‖*?'\"\\|→")

# Builtin denylist — a span entering here is genuinely never a Rust test/fn name
