def scan_docs(
    docs: list[Path],
    *,
    docs_root: Path = DOCS_ROOT,
) -> tuple[list[Citation], list[Citation], int]:
    """Read each doc, extract + classify backtick spans, attach continuation anchors,
    expand braces.

    Returns (pre_expansion, post_expansion_for_resolution, brace_expansion_count).
    pre_expansion is used for the per-kind summary; post_expansion is what
    resolution iterates.
    """
    pre: list[Citation] = []
    for doc in docs:
        try:
            text = doc.read_text(encoding="utf-8")
        except OSError:
            continue
        spans = extract_inline_backtick_spans(text)
        bare_eager = _is_bare_eager_doc(doc, docs_root)
        for line_no, span in spans:
            c = classify_span(span, doc_path=doc, line_no=line_no, bare_eager=bare_eager)
            if c is not None:
                pre.append(c)
    pre = attach_continuation_anchors(pre)

    expanded: list[Citation] = []
    brace_expansions = 0
    for c in pre:
        if c.kind == "BRACE":
            children = expand_brace(c)
            brace_expansions += len(children)
            expanded.extend(children)
        else:
            expanded.append(c)
    return pre, expanded, brace_expansions


