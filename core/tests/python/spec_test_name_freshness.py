def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        allow_abbrev=False,
        description="Detect drift between docs/*.md test-name citations and core/ Rust code.",
    )
    parser.add_argument(
        "--include",
        action="append",
        default=[],
        metavar="GLOB",
        help="glob (relative to docs/) to add to scan set; repeatable; replaces defaults",
    )
    parser.add_argument(
        "--exclude",
        action="append",
        default=[],
        metavar="GLOB",
        help="glob (relative to docs/) to remove from scan set; repeatable",
    )
    parser.add_argument(
        "--list-files",
        action="store_true",
        help="print the resolved doc set and exit 0",
    )
    parser.add_argument(
        "--self-test",
        action="store_true",
        help="run the in-script smoke fixture and exit",
    )
    parser.add_argument(
        "--audit-allowlist",
        action="store_true",
        help="also fail if any allowlist entry now resolves (i.e. is stale)",
    )
    parser.add_argument(
        "--verbose",
        action="store_true",
        help="include resolved citations in human output",
    )
    parser.add_argument(
        "--quiet",
        action="store_true",
        help="print only PASS / FAIL line (CI use)",
    )
    return parser


def main() -> int:
    parser = _build_parser()
    args, _ = parser.parse_known_args()

    if args.self_test:
        return run_self_test()

    if not DOCS_ROOT.is_dir():
        print(f"MISSING: docs root {DOCS_ROOT}", file=sys.stderr)
        return 2
    if not CORE_ROOT.is_dir():
        print(f"MISSING: core root {CORE_ROOT}", file=sys.stderr)
        return 2

    docs = collect_doc_files(DOCS_ROOT, includes=args.include, excludes=args.exclude)

    if args.list_files:
        for path in docs:
            print(path.relative_to(REPO_ROOT))
        return 0

    try:
        allowlist = load_allowlist(ALLOWLIST_PATH)
    except AllowlistMalformed as e:
        print(f"MISSING: malformed allowlist near {e}", file=sys.stderr)
        return 2

    fn_idx = build_rust_fn_index(CORE_ROOT)
    type_idx = build_rust_type_index(CORE_ROOT)
    pre, expanded, brace_expansions = scan_docs(docs)
    raw_results = [resolve_citation(c, fn_idx, type_idx) for c in expanded]
    live_results, suppressed = filter_allowlisted(raw_results, allowlist, REPO_ROOT)

    stale: list[AllowlistEntry] = []
    if args.audit_allowlist:
        stale = audit_allowlist(allowlist, pre, fn_idx, type_idx, REPO_ROOT)

    text, all_ok = render_human(
        len(docs), pre, brace_expansions, live_results,
        verbose=args.verbose, quiet=args.quiet,
        suppressed_count=len(suppressed),
        stale_allowlist=stale,
    )
    sys.stdout.write(text)

    fail_reasons: list[str] = []
    if not all_ok:
        unresolved = [r for r in live_results if r.status != "RESOLVED"]
        fail_reasons.append(f"{len(unresolved)} unresolved citation(s)")
    if stale:
        fail_reasons.append(f"{len(stale)} stale allowlist entry/entries")
    if fail_reasons:
        print(f"FAIL: {'; '.join(fail_reasons)}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
