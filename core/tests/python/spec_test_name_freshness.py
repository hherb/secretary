# Regexes for the five recognisers. Order matters in classify_span.
_RE_RICH = re.compile(
    r"^(core/[A-Za-z0-9_/\-.]+\.rs)::([A-Za-z_][A-Za-z0-9_]*)$"
)
# BRACE: optional path prefix (`core/...rs::`), then a snake_case prefix,
# then `_{a,b,c}`, then optional snake_case suffix.
_RE_BRACE = re.compile(
    r"^(?:(core/[A-Za-z0-9_/\-.]+\.rs)::)?"  # optional file
    r"([A-Za-z_][A-Za-z0-9_]*)"  # prefix
    r"_\{([a-z0-9_,]+)\}"  # _{a,b,c}
    r"(_[a-z0-9_]+)?$"  # optional trailing _suffix
)
_RE_CONTINUATION = re.compile(r"^_[a-z][a-z0-9_]*$")
_RE_QUALIFIED = re.compile(
    r"^([A-Z][A-Za-z0-9]*)::([A-Z][A-Za-z0-9_]*)$"
)
_RE_PATH_BARE = re.compile(r"^[a-z_][a-z0-9_]*$")


def strip_fenced_blocks(text: str) -> str:
    """Remove ``` ... ``` fenced blocks (and ~~~ variants).

    Leaves inline backticks alone. State machine on lines: a line whose
    *stripped* form starts with ``` or ~~~ toggles 'inside fence'.
    """
    out: list[str] = []
    in_fence = False
    for line in text.splitlines(keepends=True):
        stripped = line.lstrip()
        if stripped.startswith("```") or stripped.startswith("~~~"):
            in_fence = not in_fence
            # Replace the fence line itself with a blank line so line numbers
            # in the surviving text match the source.
            out.append("\n" if line.endswith("\n") else "")
            continue
        if in_fence:
            out.append("\n" if line.endswith("\n") else "")
        else:
            out.append(line)
    return "".join(out)


_RE_LINK = re.compile(r"\[([^\]\n]*)\]\([^)\n]*\)")
_RE_BACKTICK_SPAN = re.compile(r"`([^`\n]+)`")


def extract_inline_backtick_spans(text: str) -> list[tuple[int, str]]:
    """Return (line_no, span) for every inline-backtick span.

    Markdown-link syntax `[label](target)` is collapsed to `label` first, so
    backticks inside the label are still captured but URL targets aren't
    mined for false positives.
    """
    flat = strip_fenced_blocks(text)
    flat = _RE_LINK.sub(r"\1", flat)

    out: list[tuple[int, str]] = []
    for line_no, line in enumerate(flat.splitlines(), start=1):
        for match in _RE_BACKTICK_SPAN.finditer(line):
            out.append((line_no, match.group(1)))
    return out


def _looks_like_prose(span: str) -> bool:
    return any(ch in _SPAN_REJECT_CHARS for ch in span)


def classify_span(
    span: str,
    *,
    doc_path: Path,
    line_no: int,
    denylist: frozenset[str] = DENYLIST,
    bare_eager: bool = True,
) -> Citation | None:
    """Map one backtick span to a Citation, or None if it isn't an identifier candidate.

    Returns None for prose spans, denylisted tokens, and bare names too short
    or too generic to be plausible test names. Order matters: RICH and BRACE
    are tried before CONTINUATION/QUALIFIED/PATH_BARE so a span matching both
    falls into the more-specific bucket.

    `bare_eager` controls whether PATH_BARE classification fires for this doc.
    Spec docs (crypto-design.md, vault-format.md, glossary.md) cite wire-format
    field names that look like Rust fn names but aren't — passing False there
    suppresses the noise. RICH / BRACE / CONTINUATION / QUALIFIED still fire.
    """
    if not span or _looks_like_prose(span):
        return None
    if span in denylist:
        return None

    if m := _RE_RICH.match(span):
        return Citation(doc_path, line_no, span, "RICH", m.group(2), m.group(1))

    if m := _RE_BRACE.match(span):
        # Brace citations are placeholders; expand_brace yields the actual
        # candidates that downstream resolution sees.
        return Citation(
            doc_path,
            line_no,
            span,
            "BRACE",
            m.group(2),  # prefix
            m.group(1),  # path (or None)
        )

    if _RE_CONTINUATION.match(span):
        return Citation(doc_path, line_no, span, "CONTINUATION", span, None)

    if m := _RE_QUALIFIED.match(span):
        return Citation(doc_path, line_no, span, "QUALIFIED", m.group(2), m.group(1))

    if bare_eager and _RE_PATH_BARE.match(span):
        # Cut bare-name noise: require ≥ 6 chars AND at least one underscore.
        if len(span) < 6 or "_" not in span:
            return None
        return Citation(doc_path, line_no, span, "PATH_BARE", span, None)

    return None


def expand_brace(citation: Citation) -> list[Citation]:
    """Expand a BRACE citation into N per-element citations.

    `prefix_{a,b,c}_suffix` → [`prefix_a_suffix`, `prefix_b_suffix`, `prefix_c_suffix`].
    Each expansion becomes RICH (if the original had a path prefix) or PATH_BARE.
    Non-BRACE inputs are returned unchanged in a single-element list.
    """
    if citation.kind != "BRACE":
        return [citation]
    m = _RE_BRACE.match(citation.raw_span)
    if not m:
        return []
    file_path, prefix, csv, trailing = m.group(1), m.group(2), m.group(3), m.group(4) or ""
    parts = [p.strip() for p in csv.split(",") if p.strip()]
    out: list[Citation] = []
    for part in parts:
        name = f"{prefix}_{part}{trailing}"
        out.append(
            Citation(
                citation.doc_path,
                citation.line_no,
                f"{citation.raw_span}→{name}",
                "RICH" if file_path else "PATH_BARE",
                name,
                file_path,
            )
        )
    return out


def attach_continuation_anchors(citations: Iterable[Citation]) -> list[Citation]:
    """For each CONTINUATION, fill in `path_hint`/`anchor_file` from the most
    recent non-CONTINUATION citation on the SAME line (and same doc).

    Continuations whose anchor cannot be determined (e.g. they appear before
    any anchor on the line) are left with path_hint=None; resolution will
    flag them as `CONTINUATION-UNRESOLVED` later.
    """
    out: list[Citation] = []
    last_anchor: Citation | None = None
    last_anchor_key: tuple[Path, int] | None = None
    for c in citations:
        if c.kind != "CONTINUATION":
            out.append(c)
            if c.kind != "BRACE":  # BRACE is a placeholder; not a useful anchor
                last_anchor = c
                last_anchor_key = (c.doc_path, c.line_no)
            continue
        if last_anchor is not None and last_anchor_key == (c.doc_path, c.line_no):
            out.append(
                Citation(
                    c.doc_path,
                    c.line_no,
                    c.raw_span,
                    "CONTINUATION",
                    c.name,
                    last_anchor.name,
                    last_anchor.path_hint if last_anchor.kind == "RICH" else None,
                )
            )
        else:
            # Orphan continuation; preserve as-is so resolution can flag it.
            out.append(c)
    return out


