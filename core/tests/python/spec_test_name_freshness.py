def collect_doc_files(
    docs_root: Path,
    *,
    default_globs: tuple[str, ...] = DEFAULT_DOC_GLOBS,
    includes: list[str] | None = None,
    excludes: list[str] | None = None,
) -> list[Path]:
    """Resolve the doc set to scan.

    `includes` (if non-empty) replaces the default glob set entirely.
    `excludes` is applied last and removes any matching path.
    All globs are interpreted relative to `docs_root`.
    """
    seen: set[Path] = set()
    ordered: list[Path] = []
    glob_set = tuple(includes) if includes else default_globs

    for pattern in glob_set:
        for path in sorted(docs_root.glob(pattern)):
            if path.is_file() and path not in seen:
                seen.add(path)
                ordered.append(path)

    if excludes:
        excluded: set[Path] = set()
        for pattern in excludes:
            for path in docs_root.glob(pattern):
                excluded.add(path.resolve())
        ordered = [p for p in ordered if p.resolve() not in excluded]

    return ordered


