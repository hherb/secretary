ResolutionStatus = Literal[
    "RESOLVED",
    "RICH-MISMATCH",
    "RICH-FILE-MISSING",
    "BARE-UNRESOLVED",
    "CONTINUATION-UNRESOLVED",
    "QUALIFIED-UNRESOLVED",
]


@dataclass(frozen=True)
class ResolutionResult:
    citation: Citation
    status: ResolutionStatus
    detail: str  # human-readable reason or resolved-name


def _rich_path_in_index(name: str, cited_rel_path: str, fn_idx: dict[str, list[Path]]) -> bool:
    """True if `name` is in the fn index under a path matching `cited_rel_path` (relative to repo root)."""
    paths = fn_idx.get(name, [])
    for p in paths:
        try:
            rel = p.relative_to(REPO_ROOT)
        except ValueError:
            rel = p
        if str(rel) == cited_rel_path:
            return True
    return False


def _try_continuation_strip_search(
    anchor_name: str,
    suffix: str,
    fn_idx: dict[str, list[Path]],
    *,
    constrain_file: str | None = None,
) -> str | None:
    """Strip 1..N segments from `anchor_name`, append `suffix`, return first match.

    `suffix` includes its leading underscore. If `constrain_file` is set
    (RICH anchor), the candidate must additionally live in that file.
    """
    parts = anchor_name.split("_")
    # Strip 1 segment, then 2, …, leaving at least 1 segment of prefix.
    for strip in range(1, len(parts)):
        prefix_parts = parts[:-strip]
        if not prefix_parts:
            break
        candidate = "_".join(prefix_parts) + suffix
        if candidate not in fn_idx:
            continue
        if constrain_file is None:
            return candidate
        if _rich_path_in_index(candidate, constrain_file, fn_idx):
            return candidate
    return None


def _qualified_member_in_file(member: str, type_paths: list[Path]) -> bool:
    """Cheap check: does any file declaring the type contain the variant/const?

    Two acceptable forms in the file: `Member` as an enum-variant token (followed
    by `,`/whitespace/`{`/`(`) or `Member` as a const declaration. We grep
    each file's text for word-boundary occurrences of `member` outside comments;
    the false-positive rate here is the price of a cheap heuristic but is low
    in practice because variant/const names are CamelCase or SCREAMING_SNAKE.
    """
    if not type_paths:
        return False
    pat = re.compile(rf"\b{re.escape(member)}\b")
    for path in type_paths:
        try:
            text = path.read_text(encoding="utf-8", errors="replace")
        except OSError:
            continue
        if pat.search(text):
            return True
    return False


def resolve_citation(
    c: Citation,
    fn_idx: dict[str, list[Path]],
    type_idx: dict[str, list[Path]],
) -> ResolutionResult:
    """Decide whether `c` resolves against the Rust source, returning a typed status."""
    if c.kind == "RICH":
        cited_path = REPO_ROOT / (c.path_hint or "")
        if not cited_path.is_file():
            return ResolutionResult(c, "RICH-FILE-MISSING", f"cited file not on disk: {c.path_hint}")
        if c.name in fn_idx:
            if not _rich_path_in_index(c.name, c.path_hint or "", fn_idx):
                elsewhere = [
                    str(p.relative_to(REPO_ROOT)) for p in fn_idx[c.name][:3]
                ]
                return ResolutionResult(
                    c,
                    "RICH-MISMATCH",
                    f"`fn {c.name}` exists but not in cited file; found in: {', '.join(elsewhere)}",
                )
            return ResolutionResult(c, "RESOLVED", str(cited_path.relative_to(REPO_ROOT)))
        # Not a fn — accept if it's a const/type declared in the cited file
        # (`TARGETS` etc. live in type_idx because they match `const NAME:`).
        if c.name in type_idx and _rich_path_in_index(c.name, c.path_hint or "", type_idx):
            return ResolutionResult(c, "RESOLVED", str(cited_path.relative_to(REPO_ROOT)))
        return ResolutionResult(c, "RICH-MISMATCH", f"no `fn {c.name}` anywhere under core/")

    if c.kind == "PATH_BARE":
        if c.name in fn_idx:
            paths = fn_idx[c.name]
            return ResolutionResult(c, "RESOLVED", str(paths[0].relative_to(REPO_ROOT)))
        return ResolutionResult(c, "BARE-UNRESOLVED", f"no `fn {c.name}` anywhere under core/")

    if c.kind == "CONTINUATION":
        anchor_name = c.path_hint
        if not anchor_name:
            return ResolutionResult(
                c,
                "CONTINUATION-UNRESOLVED",
                "no anchor citation on same line",
            )
        match = _try_continuation_strip_search(
            anchor_name, c.name, fn_idx, constrain_file=c.anchor_file
        )
        if match is not None:
            return ResolutionResult(c, "RESOLVED", match)
        constraint = f" (constrained to {c.anchor_file})" if c.anchor_file else ""
        return ResolutionResult(
            c,
            "CONTINUATION-UNRESOLVED",
            f"no strip-N expansion of {anchor_name!r}+{c.name!r} matched fn index{constraint}",
        )

    if c.kind == "QUALIFIED":
        type_name = c.path_hint or ""
        type_paths = type_idx.get(type_name, [])
        if not type_paths:
            return ResolutionResult(
                c,
                "QUALIFIED-UNRESOLVED",
                f"type `{type_name}` not declared anywhere under core/",
            )
        # Standard / derived trait methods (Drop, Clone, Debug, ...) won't
        # appear as literal tokens in the impl file when they're synthesised
        # by `#[derive(...)]`. Accept on type-existence alone.
        if c.name in DERIVED_TRAIT_METHODS:
            return ResolutionResult(
                c,
                "RESOLVED",
                f"derived trait method on type in {type_paths[0].relative_to(REPO_ROOT)}",
            )
        if not _qualified_member_in_file(c.name, type_paths):
            files = ", ".join(str(p.relative_to(REPO_ROOT)) for p in type_paths[:3])
            return ResolutionResult(
                c,
                "QUALIFIED-UNRESOLVED",
                f"`{c.name}` not found in declaring file(s): {files}",
            )
        return ResolutionResult(
            c,
            "RESOLVED",
            str(type_paths[0].relative_to(REPO_ROOT)),
        )

    # BRACE should never reach resolution (expand_brace runs first); treat as unresolved.
    return ResolutionResult(c, "BARE-UNRESOLVED", f"unexpanded BRACE citation: {c.raw_span}")


