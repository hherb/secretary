@dataclass(frozen=True)
class AllowlistEntry:
    """One per-citation exception. `key` matches `<doc-rel-path>::<raw_span>`.

    `line_no` is the source line in the allowlist file (for malformed-file
    error reporting). `reason` is the user-supplied justification from the
    `# reason:` line that must immediately precede each entry.
    """

    key: str
    reason: str
    source_line: int


class AllowlistMalformed(ValueError):
    """Raised by load_allowlist when the file format is violated."""

    def __init__(self, line_no: int, message: str):
        super().__init__(f"line {line_no}: {message}")
        self.line_no = line_no
        self.message = message


def load_allowlist(path: Path) -> list[AllowlistEntry]:
    """Parse the allowlist file. Empty / missing file → empty list.

    Format (line-by-line state machine):
        # reason: <single-line justification>
        <doc-rel-path>::<exact-backtick-span>

    A `# reason:` line MUST immediately precede each entry (blank lines and
    `#`-comment lines OK between entries). Any other arrangement → error.
    """
    if not path.exists():
        return []
    out: list[AllowlistEntry] = []
    pending_reason: str | None = None
    pending_reason_line: int = 0
    for raw_line_no, raw in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
        line = raw.rstrip()
        stripped = line.strip()
        if not stripped:
            if pending_reason is not None:
                raise AllowlistMalformed(
                    raw_line_no, "blank line between `# reason:` and entry"
                )
            continue
        if stripped.startswith("# reason:"):
            if pending_reason is not None:
                raise AllowlistMalformed(
                    raw_line_no, "two `# reason:` lines in a row without entry"
                )
            pending_reason = stripped[len("# reason:"):].strip()
            pending_reason_line = raw_line_no
            if not pending_reason:
                raise AllowlistMalformed(raw_line_no, "`# reason:` line is empty")
            continue
        if stripped.startswith("#"):
            if pending_reason is not None:
                raise AllowlistMalformed(
                    raw_line_no,
                    "comment line between `# reason:` and entry",
                )
            continue
        # Non-comment, non-blank → must be an entry, must have a pending reason.
        if pending_reason is None:
            raise AllowlistMalformed(
                raw_line_no, "entry without preceding `# reason:` line"
            )
        out.append(AllowlistEntry(key=stripped, reason=pending_reason, source_line=pending_reason_line))
        pending_reason = None
    if pending_reason is not None:
        raise AllowlistMalformed(
            pending_reason_line, "trailing `# reason:` line without entry"
        )
    return out


def _citation_key(c: Citation, repo_root: Path) -> str:
    try:
        rel = c.doc_path.relative_to(repo_root)
    except ValueError:
        rel = c.doc_path
    return f"{rel.as_posix()}::{c.raw_span}"


def filter_allowlisted(
    results: list[ResolutionResult],
    allowlist: list[AllowlistEntry],
    repo_root: Path,
) -> tuple[list[ResolutionResult], list[ResolutionResult]]:
    """Split (live, suppressed). Allowlist matches a result by `<doc>::<raw_span>`."""
    keys = {entry.key for entry in allowlist}
    live: list[ResolutionResult] = []
    suppressed: list[ResolutionResult] = []
    for r in results:
        if r.status == "RESOLVED":
            live.append(r)
            continue
        if _citation_key(r.citation, repo_root) in keys:
            suppressed.append(r)
        else:
            live.append(r)
    return live, suppressed


def audit_allowlist(
    allowlist: list[AllowlistEntry],
    pre: list[Citation],
    fn_idx: dict[str, list[Path]],
    type_idx: dict[str, list[Path]],
    repo_root: Path,
) -> list[AllowlistEntry]:
    """Return allowlist entries whose underlying citation now resolves.

    These are 'stale' — the underlying drift was fixed but the allowlist
    entry was left behind. Surfacing them keeps the allowlist self-cleaning.
    """
    # Build key → citation map for the current scan.
    by_key: dict[str, Citation] = {}
    for c in pre:
        by_key[_citation_key(c, repo_root)] = c
    # Brace-expanded children share the original raw_span via the `→` decoration,
    # so they don't appear here; the original BRACE citation is what's keyed.
    stale: list[AllowlistEntry] = []
    for entry in allowlist:
        c = by_key.get(entry.key)
        if c is None:
            continue
        # If the citation still exists, run resolution. Brace-expand if needed.
        candidates = expand_brace(c) if c.kind == "BRACE" else [c]
        if all(
            resolve_citation(child, fn_idx, type_idx).status == "RESOLVED"
            for child in candidates
        ):
            stale.append(entry)
    return stale


