# Match `fn name(`, allowing visibility / async / const / unsafe modifiers.
# Anchored at line start (with optional whitespace) so we skip references
# inside comments/strings the vast majority of the time.
_RE_RUST_FN = re.compile(
    r"^\s*(?:pub(?:\s*\([^)]+\))?\s+)?(?:async\s+)?(?:const\s+)?(?:unsafe\s+)?"
    r"fn\s+([A-Za-z_][A-Za-z0-9_]*)"
)
# Match `enum Name`, `struct Name`, `trait Name` — capturing the type name.
_RE_RUST_TYPE = re.compile(
    r"^\s*(?:pub(?:\s*\([^)]+\))?\s+)?(?:enum|struct|trait)\s+([A-Za-z_][A-Za-z0-9_]*)"
)
# Match `const NAME: T = …` and `static NAME: T = …`.
_RE_RUST_CONST = re.compile(
    r"^\s*(?:pub(?:\s*\([^)]+\))?\s+)?(?:const|static)\s+([A-Z_][A-Z0-9_]*)\s*[:=]"
)


def _walk_rust_files(core_root: Path) -> list[Path]:
    """Enumerate `.rs` files under core/src and core/tests (excluding fuzz)."""
    out: list[Path] = []
    for sub in ("src", "tests"):
        root = core_root / sub
        if not root.is_dir():
            continue
        for path in sorted(root.rglob("*.rs")):
            out.append(path)
    return out


def _index_with_rg(core_root: Path, rg_pattern: str) -> dict[str, list[Path]] | None:
    """Run ripgrep against core/src and core/tests; return None if rg missing or fails."""
    rg = shutil.which("rg")
    if rg is None:
        return None
    cmd = [
        rg,
        "--type", "rust",
        "--no-heading",
        "--with-filename",
        "--line-number",
        "--only-matching",
        "--replace", "$1",
        "--multiline-dotall",  # tolerate macro spans; harmless for our regexes
        rg_pattern,
        str(core_root / "src"),
        str(core_root / "tests"),
    ]
    try:
        proc = subprocess.run(
            cmd,
            check=False,
            capture_output=True,
            text=True,
            timeout=30,
        )
    except (OSError, subprocess.SubprocessError):
        return None
    # rg exits 1 when no matches; treat both 0 and 1 as success, anything else as fallback.
    if proc.returncode not in (0, 1):
        return None
    out: dict[str, list[Path]] = defaultdict(list)
    for line in proc.stdout.splitlines():
        # Format: <file>:<line>:<captured>
        parts = line.split(":", 2)
        if len(parts) != 3:
            continue
        file_str, _, captured = parts
        out[captured].append(Path(file_str))
    return dict(out)


def _index_with_python(core_root: Path, regex: re.Pattern[str]) -> dict[str, list[Path]]:
    """Stdlib fallback: walk every .rs file and apply `regex` line by line."""
    out: dict[str, list[Path]] = defaultdict(list)
    for path in _walk_rust_files(core_root):
        try:
            text = path.read_text(encoding="utf-8", errors="replace")
        except OSError:
            continue
        for line in text.splitlines():
            if m := regex.match(line):
                out[m.group(1)].append(path)
    return dict(out)


def build_rust_fn_index(core_root: Path) -> dict[str, list[Path]]:
    """Map every `fn name` declared under core/src + core/tests to the file(s) it lives in."""
    rg_pattern = (
        r"^\s*(?:pub(?:\s*\([^)]+\))?\s+)?(?:async\s+)?(?:const\s+)?(?:unsafe\s+)?"
        r"fn\s+([A-Za-z_][A-Za-z0-9_]*)"
    )
    if (idx := _index_with_rg(core_root, rg_pattern)) is not None:
        return idx
    return _index_with_python(core_root, _RE_RUST_FN)


def build_rust_type_index(core_root: Path) -> dict[str, list[Path]]:
    """Map every `enum/struct/trait/const/static NAME` to the file(s) it lives in.

    Used for QUALIFIED-citation resolution (`Type::Variant`, `Type::CONST`).
    Two regex passes — one for type defs, one for consts/statics — merged.
    """
    type_pattern = (
        r"^\s*(?:pub(?:\s*\([^)]+\))?\s+)?(?:enum|struct|trait)\s+([A-Za-z_][A-Za-z0-9_]*)"
    )
    const_pattern = (
        r"^\s*(?:pub(?:\s*\([^)]+\))?\s+)?(?:const|static)\s+([A-Z_][A-Z0-9_]*)\s*[:=]"
    )
    out: dict[str, list[Path]] = defaultdict(list)
    for pattern, py_re in (
        (type_pattern, _RE_RUST_TYPE),
        (const_pattern, _RE_RUST_CONST),
    ):
        idx = _index_with_rg(core_root, pattern)
        if idx is None:
            idx = _index_with_python(core_root, py_re)
        for name, paths in idx.items():
            out[name].extend(paths)
    # De-dup paths per name.
    return {name: list(dict.fromkeys(paths)) for name, paths in out.items()}


