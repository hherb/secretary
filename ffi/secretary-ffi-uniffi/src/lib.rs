//! uniffi bindings for secretary-core.
//!
//! The crate-level `#![allow(unsafe_code)]` is the minimal escape hatch
//! for `uniffi::include_scaffolding!()`, which expands to the generated
//! `secretary.uniffi.rs` file in `OUT_DIR` containing `unsafe extern "C"`
//! blocks (the FFI bridge to the C ABI is inherently unsafe). The
//! crate-local lint relaxation (workspace `forbid` → crate-local `deny`)
//! is required because `forbid` is non-overridable by inner `#[allow]`;
//! see Cargo.toml.
//!
//! The `#[allow]` is **crate-level** rather than item-level because the
//! `include_scaffolding!()` macro generates code at crate scope (the
//! generated file declares `extern "C"` symbols and free functions). A
//! narrower item-level `#[allow]` doesn't cover that expansion. The
//! tradeoff: a future contributor who adds a hand-rolled `unsafe` block
//! anywhere in this crate gets silence rather than a `deny` error. The
//! crate is intentionally tiny and reviewed; new `unsafe` blocks should
//! be challenged in code review.
//!
//! Rationale: secretary_next_session.md "Begin Sub-project B.1.1".

#![allow(unsafe_code)]

// Pulls in the Rust scaffolding generated by build.rs from
// `src/secretary.udl`. The argument must match the UDL `namespace` block.
uniffi::include_scaffolding!("secretary");

/// Smoke test: returns the vault format version exposed by the core crate.
///
/// Mirrors the Python crate's `version()` for parity across the two
/// binding flavors. B.1.1 keeps it as a Rust free function so the unit
/// tests below can exercise it without going through uniffi.
pub fn version() -> u16 {
    secretary_core::version::FORMAT_VERSION
}

/// Smoke-test addition. B.1.1 round-trip target.
///
/// Uses `wrapping_add` to make the overflow contract explicit (matches
/// default Rust `+` semantics in release builds, which silently wrap),
/// matching the Python crate's `add` contract for cross-binding parity.
/// B.2 will reconsider when fallible crypto operations make uniffi's
/// error-marshalling story first-class.
pub fn add(a: u32, b: u32) -> u32 {
    a.wrapping_add(b)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn version_returns_format_version() {
        assert_eq!(version(), secretary_core::version::FORMAT_VERSION);
    }

    #[test]
    fn add_returns_arithmetic_sum() {
        assert_eq!(add(2, 3), 5);
    }

    #[test]
    fn add_wraps_on_overflow() {
        // Pin the wrapping contract: u32::MAX + 1 wraps to 0. A future
        // change to checked_add / saturating_add (or a switch to a fallible
        // signature in B.2) is a deliberate test failure rather than a
        // silent contract change.
        assert_eq!(add(u32::MAX, 1), 0);
    }
}
